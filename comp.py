#반복문 비교
a=0

#while문
names = ['john','james','tony','hun']
while a < len(names) :
  print(names[a])
  a = a + 1

#for문
names = ['john','james','tony','hun']
for name in names :
	print(name)


#for문 반복횟수 지정
for num in range(9):
  print(num)

#시작숫자 끝낼숫자 지정
for num in range(5,10):
  print(num)

